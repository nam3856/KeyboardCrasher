    public static string GenerateKoreanNickname()
    {
        string adj = adjectives[Random.Range(0, adjectives.Length)];

        string noun = nouns[Random.Range(0, nouns.Length)];
        while (adj.Length + noun.Length > 12)
        {
            noun = nouns[Random.Range(0, nouns.Length)];
        }
        return adj + noun;
    }
